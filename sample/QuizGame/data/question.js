function Quiz() {

	this.app = null;

	this.setApp = function(app) {
		this.app = app;
	};

	this.setExpression = function(expression){
		if(this.app.live2DMgr.models.length) {
			this.app.live2DMgr.models[0].setExpression(expression);
		}
	}

	this.questions = populateQuestions(quizQuestions[0]);
	this.score = 77;

}

function populateQuestions(dataObject){
	var questionArray = [];
	for(var key in dataObject){
		console.log("??");
		var question = {};
		question.text = dataObject[key][0]["text"];
		question.answers = dataObject[key][0]["answers"];
		questionArray.push(question);
	}
	return questionArray;
}

